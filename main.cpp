glm::vec3 CalculatePoint(glm::vec3 point1, glm::vec3 point2, glm::vec3 point3, float lerpArg) {
	glm::vec3 vec12 = point2 - point1;
	glm::vec3 vec23 = point3 - point2;

	glm::vec3 point12 = point1 + vec12 * lerpArg;
	glm::vec3 point23 = point2 + vec23 * lerpArg;

	glm::vec3 vecNew = point23 - point12;
	glm::vec3 result = point12 + vecNew * lerpArg;
	return result;
}

glm::vec3 GetPoint(std::vector<glm::vec3> points, float lerpArg)
{
	return CalculatePoint(points[0], points[1], points[2], lerpArg);
}

void CalcPoint(std::vector<glm::vec3> points, float lerpArg, std::vector<glm::vec3>& tmpVector) {
	if (points.size() < 3) {
		return;
	}

	//最终形成贝塞尔曲线的点
	if (points.size() == 3) {
		tmpVector.push_back(CalculatePoint(points[0], points[1], points[2], lerpArg));
		return;
	}

	//生成的中间点
	glm::vec3 tmpPoint = GetPoint(points, lerpArg);
	tmpVector.push_back(tmpPoint);
	points.erase(points.begin());
	CalcPoint(points, lerpArg, tmpVector);
}

glm::vec3 DrawBezier(std::vector<glm::vec3>& points, float lerpArg) {
	if (points.size() == 3) {
		return GetPoint(points, lerpArg);
	}
	if (points.size() > 3) {
		glm::vec3 result(0);
		std::vector<glm::vec3> tmpPoints;
		CalcPoint(points, lerpArg, tmpPoints);
		if (tmpPoints.size() >= 3)
			return DrawBezier(tmpPoints, lerpArg);
		else 
		{
			 result = tmpPoints[0] + lerpArg * (tmpPoints[1] - tmpPoints[0]);
		}
		return result;
	}
	else
	{
		return glm::vec3(0);
	}
}


